function calcular() {
  var origem = document.getElementById("origem").value;
  var destino = document.getElementById("destino").value;

  if (!origem || !destino) {
    document.getElementById("resultado").innerHTML = "‚ö†Ô∏è Informe origem e destino.";
    return;
  }

  var service = new google.maps.DistanceMatrixService();
  service.getDistanceMatrix(
    {
      origins: [origem],
      destinations: [destino],
      travelMode: 'DRIVING',
      unitSystem: google.maps.UnitSystem.METRIC
    },
    function (response, status) {
      if (status === 'OK') {
        var resultado = response.rows[0].elements[0];
        if (resultado.status === "OK") {
          document.getElementById("resultado").innerHTML =
            "üöö Dist√¢ncia: <strong>" + resultado.distance.text + "</strong><br>" +
            "‚è±Ô∏è Tempo estimado: <strong>" + resultado.duration.text + "</strong>";
        } else {
          document.getElementById("resultado").innerHTML = "N√£o foi poss√≠vel calcular a rota.";
        }
      } else {
        document.getElementById("resultado").innerHTML = "Erro: " + status;
      }
    }
  );
}

